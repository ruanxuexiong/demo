package com.zftlive.android.library.widget.gridview;

import android.content.Context;
import android.util.AttributeSet;
import android.widget.GridView;


/**
 * 全部展开的Listview,用于嵌套不复用的容器中
 *
 * @author 曾繁添
 * @version 1.0
 */
public class ExpansionGridView extends GridView {

    public ExpansionGridView(Context context) {
        super(context);
    }

    public ExpansionGridView(Context context, AttributeSet attrs) {
        super(context, attrs);
    }


    @Override
    public void onMeasure(int widthMeasureSpec, int heightMeasureSpec) {

        int expandSpec = MeasureSpec.makeMeasureSpec(Integer.MAX_VALUE >> 2,
                MeasureSpec.AT_MOST);
        super.onMeasure(widthMeasureSpec, expandSpec);
    }
}
